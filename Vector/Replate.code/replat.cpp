#include <iostream>
using namespace std;

class BankAccount {
protected:
    double balance;

public:

    void displayBalance() const {
        cout << "Current balance: " << balance << endl;
    }
};

class DepositAccount : public BankAccount {
public:
    void deposit() {
        double amount;
        cout << "Enter deposit amount: ";
        cin >> amount;
        if (amount > 0) {
            balance += amount;
            cout << "Deposited: " << amount << endl;
        } else {
            cout << "Invalid deposit amount" << endl;
        }
    }
};

class WithdrawalAccount : public BankAccount {
public:
    void withdraw() {
        double amount;
        cout << "Enter withdrawal amount: ";
        cin >> amount;
        if (amount > 0 && amount <= balance) {
            balance -= amount;
            cout << "Withdrawn: " << amount << endl;
        } else {
            cout << "Invalid or insufficient funds for withdrawal" << endl;
        }
    }
};

int main() {
    DepositAccount depositAccount;
    WithdrawalAccount withdrawalAccount;

    depositAccount.deposit();
    depositAccount.displayBalance();

    withdrawalAccount.withdraw();
    withdrawalAccount.displayBalance();

    return 0;
}


